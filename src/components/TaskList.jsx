// Декларативный импорт библиотечного ядра React для инициализации функционального компонента
import React from 'react';

// Импорт дочернего атомарного компонента TaskCard, отвечающего за отрисовку отдельной строки-протокола
import TaskCard from './TaskCard';

// Экспорт по умолчанию функционального компонента TaskList, принимающего деструктурированный объект входящих параметров (пропсов)
export default function TaskList({ items, role, onDelete, onStatusChange, onUpdateTask }) {
  
  // Валидация длины входящего массива данных на уровне UI: перехват пустого состояния реестра active виртуальных испытаний
  if (items.length === 0) {
    // Оператор раннего возврата (Early Return): блокирует отрисовку пустой HTML-таблицы и выводит презентационное уведомление
    return <div className="empty-msg">Реестр пуст. Добавьте первую заявку!</div>;
  }

  // Возврат декларативного дерева элементов JSX для формирования табличной структуры реестра в интерфейсе SPA
  return (
    // Инициализация стандартного HTML-тега таблицы, стилизованного через класс registry-table под стандарты PLM-систем
    <table className="registry-table">
      
      {/* Определение блока заголовка таблицы (Table Head) для статического вывода наименований расчетных полей */}
      <thead>
        {/* Формирование строки заголовков; все вложенные th-теги записаны без пробельных символов во избежание whitespace-артефактов */}
        <tr>
          <th>ID / Шифр</th>
          <th>Объект / Модель</th>
          <th>Физика (CAE)</th>
          <th>Сетка (FEM)</th>
          <th>Приоритет</th>
          <th>Стоимость (SLA)</th>
          <th>Результаты расчетов</th>
          <th>Дедлайн</th>
          <th>Статус</th>
          <th>Действие</th>
        </tr>
      </thead>
      
      {/* Определение контейнера тела таблицы (Table Body) для размещения динамически генерируемых строк */}
      <tbody>
        {/* Декларативная итерация по обработанному массиву инженерных задач items с использованием встроенного метода map */}
        {items.map(item => (
          // Инстанцирование компонента строки-карточки TaskCard для каждого обособленного объекта задачи в массиве
          <TaskCard
            // Назначение обязательного системного пропса key, содержащего UUID задачи, для корректной работы алгоритма сверки Virtual DOM
            key={item.id} 
            // Использование оператора деструктуризации spread (...) для сквозного проброса всех внутренних физических свойств объекта
            {...item}      
            // Передача флага текущей роли пользователя с целью жесткого разграничения прав доступа на уровне дочерней строки
            role={role}    
            // Ретрансляция колбэка деструктивного удаления записи из реестра испытаний на базе фильтрации
            onDelete={onDelete}
            // Ретрансляция колбэка оперативного изменения PLM-стадии выполнения задачи из селектора строки
            onStatusChange={onStatusChange}
            // Проброс функции полного обновления полей (Update), устраняющий замечание о разрыве цепочки трансляции пропсов
            onUpdateTask={onUpdateTask} 
          />
        ))}
      </tbody>
      
    </table>
  );
}