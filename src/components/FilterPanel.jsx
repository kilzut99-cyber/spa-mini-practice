// Декларативный импорт библиотечного ядра React для инициализации функционального компонента
import React from 'react';

// Экспорт по умолчанию функционального компонента FilterPanel, принимающего деструктурированный объект пропсов управления конвейером данных
export default function FilterPanel({ activeFilter, onFilterChange, searchTerm, onSearchChange, sortBy, onSortChange }) {
  
  // Объявление константного массива строк, представляющего регламентированные PLM-стадии жизненного цикла инженерных задач
  const stages = ["Все", "Новая", "В расчете", "Верификация", "Завершено"];

  // Возврат и отрисовка декларативного дерева элементов JSX для формирования управляющей панели интерфейса
  return (
    // Корневой контейнер панели, изолированный границами, внутренними отступами и скруглением по правилам технической эргономики
    <div className="filter-panel-container" style={{ margin: '15px 0', background: 'white', padding: '15px', border: '1px solid var(--border-grey)', borderRadius: '4px' }}>
      
      {/* Использование флекс-контейнера с поддержкой автоматического переноса ячеек и выравниванием элементов по краям оси */}
      <div style={{ display: 'flex', flexWrap: 'wrap', gap: '15px', alignItems: 'center', justifyContent: 'space-between' }}>
        
        {/* Дочерний флекс-блок, группирующий интерактивные триггеры переключения стадий контроля качества КЦ */}
        <div style={{ display: 'flex', gap: '8px' }}>
          
          {/* Декларативная итерация по массиву стадий методом map для генерации набора интерфейсных кнопок */}
          {stages.map(stage => (
            <button
              // Обязательное назначение уникального ключа key для оптимизации алгоритма сравнения элементов в Virtual DOM
              key={stage}
              // Динамическое вычисление CSS-класса: если стадия совпадает с активным стейтом, применяется класс подсветки active
              className={activeFilter === stage ? "filter-btn active" : "filter-btn"}
              // Реактивная подписка на событие клика с трансляцией строкового имени выбранной стадии в родительский App.js
              onClick={() => onFilterChange(stage)}
            >
              {/* Вывод текстового наименования текущего шага жизненного цикла на поверхность кнопки */}
              {stage}
            </button>
          ))}
        </div>

        {/* Дочерний флекс-блок, изолирующий компоненты сквозного текстового поиска и многовариантной сортировки */}
        <div style={{ display: 'flex', gap: '10px' }}>
          
          {/* Контролируемый текстовый инпут ввода поискового запроса с фиксацией внутренних отступов и радиуса скругления */}
          <input 
            type="text" 
            // Презентационный текст-подсказка, информирующий оператора о функциональном назначении элемента ввода
            placeholder="Поиск по названию..." 
            // Двустороннее реактивное связывание значения инпута со строковым состоянием searchTerm из главного компонента
            value={searchTerm}
            // Перехват события ввода символов пользователем с мгновенным подъемом нового строкового значения через колбэк
            onChange={(e) => onSearchChange(e.target.value)}
            // Кастомная инлайн-стилизация геометрических параметров текстового поля ввода
            style={{ padding: '6px 12px', border: '1px solid var(--border-grey)', borderRadius: '4px', fontSize: '0.9rem' }}
          />
          
          {/* Контролируемый селектор выпадающего списка для динамического управления упорядочиванием реестра задач */}
          <select 
            // Двустороннее связывание выбранной опции с реактивным стейтом критерия сортировки sortBy
            value={sortBy} 
            // Подписка на изменение выбора в списке с трансляцией кодового ключа наверх к источнику истины
            onChange={(e) => onSortChange(e.target.value)} 
            style={{ padding: '6px 12px', border: '1px solid var(--border-grey)', borderRadius: '4px', fontSize: '0.9rem' }}
          >
            {/* Опция 1: Критерий упорядочивания записей по хронологической дате их добавления в систему */}
            <option value="date">Сортировка: Дата создания</option>
            {/* Опция 2: Критерий упорядочивания по плановым временным меткам окончания виртуальных испытаний */}
            <option value="deadline">Сортировка: Дедлайн</option>
            {/* Опция 3: Критерий сортировки по весовым коэффициентам критичности и важности задач SLA */}
            <option value="priority">Сортировка: Приоритет</option>
            {/* Опция 4: Лексикографический критерий выравнивания строк реестра по алфавитному признаку названий деталей */}
            <option value="title">Сортировка: По алфавиту</option>
          </select>
        </div>

      </div>
    </div>
  );
}