// Декларативный импорт библиотечного ядра React и базового хука управления состоянием useState
import React, { useState } from 'react';

// Экспорт по умолчанию функционального компонента формы, принимающего деструктурированный колбэк-проп onAdd
export default function AddTaskForm({ onAdd }) {
  
  // Инициализация сложного реактивного стейта formData контролируемым объектом со всеми физическими и PLM-параметрами задачи
  const [formData, setFormData] = useState({ 
    title: '',         // Строковое наименование тестируемого узла или детали машиностроения
    category: 'Механика', // Физическая CAE-дисциплина расчета (по умолчанию установлена прочностная Механика)
    lod: 'Medium',     // Уровень плотности сетки конечных элементов (по умолчанию установлена стандартная сетка)
    priority: 'Medium', // Индекс приоритета и срочности выполнения технического задания в Консалтинговом центре
    loadValue: 100,     // Исходная величина эксплуатационной нагрузки, передаваемая в математическое ядро симулятора
    deadline: ''       // Временной штамп планируемого дедлайна сдачи протокола испытаний заказчику
  });
  
  // Инициализация изолированного реактивного стейта error для оперативного вывода сообщений валидации в UI
  const [error, setError] = useState('');

  // Декларативный метод-обработчик отправки данных при совершении события отправки формы (Submit)
  const handleSubmit = (e) => {
    e.preventDefault(); // Принудительное аппаратное блокирование дефолтной перезагрузки страницы браузером
    
    // Комплексная валидация данных: проверка на пустые строки и исключение критических неположительных нагрузок (<= 0)
    if (!formData.title.trim() || !formData.deadline || formData.loadValue <= 0) {
      setError("Заполните наименование, корректную нагрузку (>0) и дату дедлайна!"); // Запись текста ошибки в стейт
      return; // Экстренное прерывание выполнения функции с целью защиты базы данных от невалидных значений
    }
    
    setError(''); // Сброс текстового состояния ошибки при успешном прохождении верификационного фильтра
    onAdd(formData); // Трансляция и подъем накопленного буфера состояния наверх к источнику истины в родительский App.js

    // Иммутабельный сброс полей графической формы к исходным заводским параметрам по умолчанию
    setFormData({ 
      title: '', 
      category: 'Механика', 
      lod: 'Medium', 
      priority: 'Medium', 
      loadValue: 100, 
      deadline: '' 
    });
  };

  // Возврат декларативной древовидной разметки JSX для рендеринга элементов управления формы в интерфейсе SPA
  return (
    // Привязка функции-перехватчика handleSubmit к системному событию отправки формы onSubmit
    <form className="engineering-form" onSubmit={handleSubmit}>
      {/* Отрисовка текстового заголовка формы регистрации по правилам технического задания */}
      <h3>Регистрация новой задачи виртуальных испытаний</h3>
      
      {/* Контейнерная обертка строки ввода, стилизованная посредством сетки CSS Grid для адаптивного позиционирования */}
      <div className="form-row">
        
        {/* Компонент ввода 1: Контролируемый текстовый инпут названия детали со связанным событием onChange */}
        <input
          type="text"
          placeholder="Наименование детали..."
          value={formData.title} // Двустороннее связывание инпута с реактивным свойством title из стейта
          onChange={(e) => setFormData({...formData, title: e.target.value})} // Мутация свойства с сохранением остальных полей
        />

        {/* Компонент ввода 2: Выпадающий список выбора физической дисциплины с привязкой к свойству category */}
        <select value={formData.category} onChange={(e) => setFormData({...formData, category: e.target.value})}>
          <option value="Механика">Механика (Прочность МПа)</option>
          <option value="CFD">CFD (Аэродинамика м/с)</option>
          <option value="Тепло">Теплофизика (°C)</option>
        </select>

        {/* Компонент ввода 3: Выпадающий список выбора плотности дискретизации сетки FEM с привязкой к свойству lod */}
        <select value={formData.lod} onChange={(e) => setFormData({...formData, lod: e.target.value})}>
          <option value="Low">Low LOD (Грубая сетка)</option>
          <option value="Medium">Medium LOD (Стандартная)</option>
          <option value="High">High LOD (Высокоточная)</option>
        </select>

        {/* Компонент ввода 4: Выпадающий список выбора уровня приоритета SLA с привязкой к свойству priority */}
        <select value={formData.priority} onChange={(e) => setFormData({...formData, priority: e.target.value})}>
          <option value="Low">Низкий приоритет</option>
          <option value="Medium">Средний приоритет</option>
          <option value="High">Высокий приоритет</option>
        </select>
        
        {/* Компонент ввода 5: Числовой инпут контролируемого ввода величины эксплуатационной нагрузки loadValue */}
        <input
          type="number"
          placeholder="Нагрузка"
          value={formData.loadValue} // Синхронизация значения инпута с числовым свойством объекта состояния
          onChange={(e) => setFormData({...formData, loadValue: Number(e.target.value)})} // Явное приведение строки к числу
        />

        {/* Компонент ввода 6: Контролируемый календарный инпут даты планируемого завершения симуляции */}
        <input
          type="date"
          value={formData.deadline}
          onChange={(e) => setFormData({...formData, deadline: e.target.value})}
        />

        {/* Кнопка инициации отправки формы, автоматически триггерящая событие onSubmit тега form */}
        <button type="submit">Запустить симуляцию изделия</button>
      </div>
      
      {/* Декларативный условный рендеринг: блок параграфа ошибки выводится строго при наличии текста в стейте error */}
      {error && <p className="error-msg" style={{ color: '#e53e3e', fontSize: '0.85rem', fontWeight: 'bold', margin: '10px 0 0 0' }}>⚠️ Ошибка: {error}</p>}
    </form>
  );
}