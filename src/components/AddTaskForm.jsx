// Декларативный импорт библиотечного ядра React и базового хука управления состоянием useState
import React, { useState } from 'react';

// Массив константных строк-корней, содержащий регламентированные машиностроительные термины для валидации
const VALID_ENGINEERING_TERMS = [
  'вал', 'ось', 'корпус', 'редуктор', 'шестеренка', 'зубчат', 'колес', 'фланец', 
  'муфта', 'поршень', 'цилиндр', 'клапан', 'рычаг', 'кронштейн', 'балка', 'опора', 
  'шпиндель', 'турбина', 'лопатка', 'компрессор', 'насос', 'картер', 'шатун', 'шкив',
  'маховик', 'ступица', 'диск', 'шпилька', 'болт', 'гайка', 'шплинт', 'втулка'
];

// Экспорт по умолчанию функционального компонента формы, принимающего колбэк-проп добавления задачи onAdd
export default function AddTaskForm({ onAdd }) {
  
  // Инициализация сложного реактивного стейта formData контролируемым объектом со всеми физическими и PLM-параметрами задачи
  const [formData, setFormData] = useState({ 
    title: '',            // Строковое наименование тестируемого узла или детали машиностроения
    category: 'Механика', // Физическая CAE-дисциплина расчета (по умолчанию установлена Механика)
    lod: 'Medium',        // Уровень плотности сетки конечночных элементов FEM (стандартная сетка)
    priority: 'Medium',   // Индекс приоритета и срочности выполнения технического задания в Консалтинговом центре
    loadValue: 100,       // Числовая величина эксплуатационной нагрузки, передаваемая в ядро симулятора
    deadline: ''          // Временной штамп планируемого дедлайна сдачи протокола испытаний
  });

  // Инициализация изолированного стейта error для оперативного вывода сообщений валидации в интерфейс
  const [error, setError] = useState('');

  // Стрелочная функция-валидатор для сопоставления введенного текста с белым списком машиностроительной номенклатуры
  const validateEngineeringTitle = (title) => {
    // Приведение введенной строки к нижнему регистру и обрезка боковых пробелов для корректного сравнения
    const cleanedTitle = title.trim().toLowerCase();
    // Метод .some() возвращает true, если хотя бы один корень из массива содержится во введенном названии детали
    return VALID_ENGINEERING_TERMS.some(term => cleanedTitle.includes(term));
  };

  // Декларативный метод-обработчик совершения события отправки графической формы (Submit)
  const handleSubmit = (e) => {
    // Принудительное блокирование дефолтного поведения браузера для предотвращения перезагрузки страницы
    e.preventDefault(); 
    
    // Ступень 1: Комплексная валидация на пустые поля и исключение неположительных или нулевых физических нагрузок
    if (!formData.title.trim() || !formData.deadline || formData.loadValue <= 0) {
      // Запись информационного текста базовой ошибки в реактивное состояние error
      setError("Заполните наименование, корректную нагрузку (>0) и дату дедлайна!");
      // Прерывание выполнения функции с целью защиты локальной базы данных от невалидных строк
      return;
    }

    // Ступень 2: Отраслевой семантический фильтр (контроль ввода "абракадабры" на основе функции-валидатора)
    if (!validateEngineeringTitle(formData.title)) {
      // Запись сообщения о нарушении регламента номенклатуры машиностроения в стейт ошибки
      setError("Ошибка номенклатуры! Наименование должно содержать корректный машиностроительный объект (например: Вал, Корпус, Фланец, Редуктор).");
      // Экстренный выход из вычислительного конвейера отправки формы
      return;
    }

    // Полный сброс текстового состояния ошибки при успешном прохождении всех валидационных рубежей
    setError(''); 
    // Передача и подъем накопленного валидного буфера состояния наверх к источнику истины в родительский App.js
    onAdd(formData); 
    
    // Иммутабельный сброс контролируемых полей ввода формы к исходным заводским параметрам по умолчанию
    setFormData({ 
      title: '', 
      category: 'Механика', 
      lod: 'Medium', 
      priority: 'Medium', 
      loadValue: 100, 
      deadline: ''
    });
  };

  // Возврат дерева элементов JSX для рендеринга интерактивной формы в интерфейсе SPA
  return (
    // Привязка функции-перехватчика handleSubmit к системному событию отправки формы onSubmit
    <form className="engineering-form" onSubmit={handleSubmit}>
      {/* Текстовый заголовок формы регистрации по правилам технического задания */}
      <h3>Регистрация новой задачи виртуальных испытаний</h3>
      
      {/* Сетка CSS Grid для адаптивного и бесконфликтного позиционирования полей ввода */}
      <div className="form-row">
        
        {/* Поле 1: Контролируемый текстовый инпут ввода объекта со связыванием значения со стейтом title */}
        <input
          type="text"
          placeholder="Наименование детали (например: Вал шестерня)..."
          value={formData.title}
          // Обновление свойства title в стейте при вводе символов с сохранением остальных полей через spread-оператор
          onChange={(e) => setFormData({...formData, title: e.target.value})}
        />
        
        {/* Поле 2: Селектор выпадающего списка для определения CAE-дисциплины физического процесса */}
        <select value={formData.category} onChange={(e) => setFormData({...formData, category: e.target.value})}>
          <option value="Механика">Механика (Прочность МПа)</option>
          <option value="CFD">CFD (Аэродинамика м/с)</option>
          <option value="Тепло">Теплофизика (°C)</option>
        </select>
        
        {/* Поле 3: Селектор плотности дискретизации расчетной сетки для коррекции mesh-погрешности ядра */}
        <select value={formData.lod} onChange={(e) => setFormData({...formData, lod: e.target.value})}>
          <option value="Low">Low LOD (Грубая сетка)</option>
          <option value="Medium">Medium LOD (Стандартная)</option>
          <option value="High">High LOD (Высокоточная)</option>
        </select>
        
        {/* Поле 4: Селектор выбора уровня приоритета и наценки в рамках соглашения об уровне сервиса SLA */}
        <select value={formData.priority} onChange={(e) => setFormData({...formData, priority: e.target.value})}>
          <option value="Low">Низкий приоритет</option>
          <option value="Medium">Medium приоритет</option>
          <option value="High">Высокий приоритет</option>
        </select>
        
        {/* Поле 5: Числовой инпут ввода величины эксплуатационной механической или тепловой нагрузки */}
        <input
          type="number"
          placeholder="Нагрузка"
          value={formData.loadValue}
          // Мутация стейта с явным принудительным приведением строкового значения из инпута к числу через Number()
          onChange={(e) => setFormData({...formData, loadValue: Number(e.target.value)})}
        />
        
        {/* Поле 6: Календарный инпут для выбора планового дедлайна окончания численного моделирования */}
        <input
          type="date"
          value={formData.deadline}
          onChange={(e) => setFormData({...formData, deadline: e.target.value})}
        />
        
        {/* Системная кнопка запуска, автоматически вызывающая обработчик onSubmit у тега form */}
        <button type="submit">Запустить симуляцию изделия</button>
      </div>
      
      {/* Условный рендеринг: предупреждающий блок параграфа выводится строго при наличии текста в стейте error */}
      {error && <p className="error-msg" style={{ color: '#e53e3e', fontSize: '0.85rem', fontWeight: 'bold', margin: '10px 0 0 0' }}>⚠️ Ошибка: {error}</p>}
    </form>
  );
}